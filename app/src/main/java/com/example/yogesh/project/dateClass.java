package com.example.yogesh.project;

public class dateClass {
    private int day, month, year;

    public dateClass() {

    }

    public dateClass(int day, int month, int year) {
        this.day = day;
        this.month = month;
        this.year = year;
    }

    public int getDay() {
        return day;
    }

    public int getMonth() {
        return month;
    }

    public int getYear() {
        return year;
    }
}
